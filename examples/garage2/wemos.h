// GPIO PIN names as written on the breakout boards for
// various ESP8266 chips, including the WEMOS D1

#define GPIO_D0 16
#define GPIO_D1  5  // SCL
#define GPIO_D2	 4  // SDA
#define GPIO_D3	 0  // 10k Pull-up
#define GPIO_D4	 2  // 10k Pull-up, BUILTIN_LED
#define GPIO_D5	14 // SCK
#define GPIO_D6	12 // MISO
#define GPIO_D7	13 // MOSI
#define GPIO_D8	15 // 10k Pull-down, SS
#define GPIO_LED GPIO_D4
